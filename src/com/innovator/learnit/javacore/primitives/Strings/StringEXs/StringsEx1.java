package com.innovator.learnit.javacore.primitives.Strings.StringEXs;

public class StringsEx1 {
	public static void main(String[] args) {
		double math = Math.PI;
		System.out.printf("%.1f %n", math);
		System.out.printf("%.2f %n", math);
		System.out.printf("%.3f %n", math);
		System.out.printf("%.4f %n", math);
		System.out.printf("%.5f %n", math);
	}

}
