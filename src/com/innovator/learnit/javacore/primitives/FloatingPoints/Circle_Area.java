package com.innovator.learnit.javacore.primitives.FloatingPoints;

//Compute the area of a circle.

public class Circle_Area {
	public static void main(String[] args) {
		double r, a, pi;
		
		//radius
		r = 10.16;
		
		//pi
		pi = 3.1416;
		
		//area
		a = pi * r * r;
		
		System.out.println("Area of circle is " + a);
	}

}
