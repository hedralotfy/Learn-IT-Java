package com.innovator.learnit.javacore.primitives;

public class String_literals {

	public static void main(String[] args) {
		System.out.println("Hello World!");
		System.out.println("Two\nLines");
		System.out.print("He got enraged ");
		System.out.print("and before he left the room,");
		System.out.println(" he shouted: \"THAT'S INSANE!\"");
		System.out.println("\"This is quoted\"");
		System.out.println("\t\t\t\tA message to my friend");
		System.out.println("Dear friend,");
		System.out.println("\tI hope you're doing well,"
				+ " I'm happy I'm writing to you again.\n"
				+ "I'm inviting you to join my team as it is"
				+ "an amazing team, you're gonna love"
				+ " working with us.\n\n"
				+ "\t\t\t\t\t\t\tyours, Hedra");

	}

}
